<h1>Guia de estudio de dibujo en 3 partes</h1></br>



<p>
<h5>Parte 1: Fundamentos del dibujo (4 semanas)</h5></br>

<h5>Semana 1:</h5>

Introducción al dibujo: materiales básicos, técnicas de agarre del lápiz, postura correcta.</br>
Conceptos básicos de perspectiva: línea, forma, volumen, profundidad.</br>
Ejercicios de dibujo de líneas rectas, curvas y formas básicas (círculos, cuadrados, triángulos).</br></br>
<h5>Semana 2:</h5></br>

Luces y sombras: comprensión de la luz y la sombra, técnicas de sombreado.</br>
Texturas: representación de diferentes texturas en papel (madera, tela, metal, etc.).</br>
Ejercicios de dibujo de objetos simples con luces y sombras.</br></br>
<h5>Semana 3:</h5></br>

Proporción y composición: reglas de proporción en el dibujo, composición básica de una imagen.</br>
Perspectiva de uno y dos puntos: construcción de objetos en perspectiva.</br>
Ejercicios de dibujo de objetos más complejos con perspectiva y composición.</br></br>
<h5>Semana 4:</h5></br>

Anatomía humana básica: proporciones del cuerpo humano, estructuras básicas (cabeza, torso, extremidades).</br>
Ejercicios de dibujo de la figura humana en diferentes poses.</br></br>
<h5>Parte 2: Técnicas de dibujo (4 semanas)</h5></br>
</br>
<h5>Semana 5:</h5></br>

Técnicas de lápiz: diferentes tipos de lápices, técnicas de sombreado y textura con lápiz.</br>
Ejercicios de dibujo de diferentes texturas utilizando técnicas de lápiz.</br></br>
<h5>Semana 6:</h5></br>

Tinta y carboncillo: técnicas de dibujo con tinta y carboncillo, texturas y efectos.</br>
Ejercicios de dibujo de objetos y paisajes utilizando tinta y carboncillo.</br></br>
<h5>Semana 7:</h5></br>

Acuarela: introducción a la acuarela, técnicas básicas de mezcla y aplicación.</br>
Ejercicios de pintura de paisajes y flores con acuarela.</br></br>
<h5>Semana 8:</h5></br>

Pastel y óleo: técnicas básicas de pastel y óleo, mezcla de colores, creación de texturas.</br>
Ejercicios de pintura de retratos y bodegones con pastel y óleo.</br></br>
<h5>Parte 3: Desarrollo del estilo propio (4 semanas)</h5></br>

<h5>Semana 9:</h5></br>

Experimentación con diferentes técnicas y materiales: búsqueda de un estilo personal.</br>
Desarrollo de un proyecto de dibujo personal: tema libre, aplicación de las técnicas aprendidas.</br></br>
<h5>Semana 10:</h5></br>

Refinamiento del estilo propio: exploración de diferentes estilos de dibujo, artistas como referencia.</br>
Continuación del proyecto de dibujo personal.</br></br>
<h5>Semana 11:</h5></br>

Crítica constructiva: compartir el trabajo con otros artistas, recibir y dar feedback.</br>
Finalización del proyecto de dibujo personal.</br></br>
<h5>Semana 12:</h5></br>

Presentación del proyecto de dibujo personal: exposición o portafolio digital.</br>
Reflexión sobre el proceso de aprendizaje y desarrollo del estilo propio.</br></br>
<h6>Recursos adicionales:</h6></br>

Libros de dibujo y anatomía.</br>
Tutoriales en línea y videos de artistas.</br>
Clases de dibujo presenciales o en línea.</br>
Museos y galerías de arte.</br>




