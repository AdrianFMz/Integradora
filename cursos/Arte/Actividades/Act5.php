<h1>Composición:</h1>


<p>
<h5>Primera composición / boceto</h5>  </br>
El primer boceto es el más básico de todos. Encontramos a la persona meditando en el mismo plano que el lobo,  </br>
 siempre acechando desde las sombras de los arbustos.

 </br> </br>
<img src="http://localhost/integradora/imagenes/com1.png" width="30%" height="30%" class="d-inline-block align-top" alt="">
</br></br>

Vemos que si ponemos los rectángulos y la espiral de Fibonacci, el foco estará en la persona meditando.   </br>
También vemos que la vista en esta composición saldrá por donde entra el lobo.


</br> </br>
<img src="http://localhost/integradora/imagenes/com2.png" width="30%" height="30%" class="d-inline-block align-top" alt="">
</br></br>

En cuanto a la regla de los tercios, vemos que las líneas verticales y horizontales se cruzan a la espalda  </br>
 de la persona que quiero enfocar. Así que en términos de esta regla, esta composición funciona.  </br>  </br>

 <h5>Segunda composición / boceto</h5>
En esta segunda composición, pongo a la persona que medita en primer plano y bastante grande.   </br>
El lobo viene de abajo hacia arriba, acechando a la persona.

</br> </br>
<img src="http://localhost/integradora/imagenes/com3.png" width="30%" height="30%" class="d-inline-block align-top" alt="">
</br></br>

Con la guía de Fibonacci, vemos que el cuadrado da precisamente en la misma cara que ella.  </br>
 La espiral también se cierra sobre la cara y sale por donde está el lobo. Así que ésta   </br>
 es también una composición que aplica la proporción áurea de Fibonacci.

 </br> </br>
<img src="http://localhost/integradora/imagenes/com4.png" width="30%" height="30%" class="d-inline-block align-top" alt="">
</br></br>

También aplica la guía de los tercios señalando los focos en la cara de la persona y el horizonte</br>
 en la línea de arriba.</br></br>

<h5>Tercera composición / boceto</h5></br>
En la tercera opción, pongo al lobo en la parte inferior en lugar de la superior, mirando hacia la</br>
 persona que acecha.

 </br> </br>
<img src="http://localhost/integradora/imagenes/com5.png" width="30%" height="30%" class="d-inline-block align-top" alt="">
</br></br>

El centro de atención es el hombro de la persona, cerca de la cara, y se da la mayor cantidad de luz. </br>
La espiral sale donde está el lobo.

</br> </br>
<img src="http://localhost/integradora/imagenes/com6.png" width="30%" height="30%" class="d-inline-block align-top" alt="">
</br></br>

Utilizando la regla de los tercios, vemos que uno de los puntos se cruza en la zona superior de la espalda,</br>
 muy cerca de la cara. También podemos ver la línea del horizonte que coincide con la horizontal de arriba.


 </br> </br>
<img src="http://localhost/integradora/imagenes/com7.png" width="30%" height="30%" class="d-inline-block align-top" alt="">
</br></br>

