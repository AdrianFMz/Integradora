</br></br>

En el cuello, la cervical (1) se ubica justo detrás de la mandíbula (2), hay una variedad de músculos que </br>
intervienen en el movimiento de la cabeza, el que más salta a la vista tiene un nombre muy, muy largo… </br>
(esternocleidomastoideo ¡uff!) pero lo puedes reconocer fácilmente porque tiene forma de V, y parte desde </br>
la oreja hasta el centro de las clavículas (3). En el centro de estos músculos se encuentra la nuez, que es</br>
 más prominente en los hombres (4).

 </br> </br>
<img src="http://localhost/integradora/imagenes/ana2.png" width="30%" height="30%" class="d-inline-block align-top" alt="">
</br></br>
 

La caja torácica es la base que articula los brazos. Puedes dibujarla de muchas maneras, pero a mí me </br>
gusta darle una forma ovoide que se asemeja más al contorno de las costillas (1).

</br> </br>
<img src="http://localhost/integradora/imagenes/ana3.png" width="30%" height="30%" class="d-inline-block align-top" alt="">
</br></br>
 

El esternón (2) cierra esta estructura en la parte frontal. Este hueso y la columna vertebral crean líneas </br>
imaginarias que dividen el cuerpo humano en dos, ¡úsalas como guía!</br></br>

Las clavículas (3) son similares a un manillar de bicicleta. Puedes pensar en ellas como un soporte para</br>
 los hombros; cada vez que los brazos se muevan, estas cambiarán de dirección.

 </br> </br>
<img src="http://localhost/integradora/imagenes/ana4.png" width="30%" height="30%" class="d-inline-block align-top" alt="">
</br></br>

En la espalda encontrarás las escápulas (4), un par de huesos de forma triangular que aportarán movimiento a los brazos.</br> 
La espalda sufre cambios según los movimientos de estos huesos.


</br> </br>
<img src="http://localhost/integradora/imagenes/ana5.png" width="30%" height="30%" class="d-inline-block align-top" alt="">
</br></br>

La pelvis, situada al final del torso, se conecta a la columna lumbar desde el sacro (1) . A ambos lados podrás observar los </br>
coxales (2); y en la parte frontal, la zona del pubis (3).


