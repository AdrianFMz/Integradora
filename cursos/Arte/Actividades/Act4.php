<h1>Anatomia Humana:</h1>

<p>

Se trata de ponerle empeño y corazón a lo que hacemos. Dibujar anatomía no es sencillo, pero confío en que este </br>
artículo podrá guiarte y animarte a seguir aprendiendo. Empezaremos con las partes que esencialmente conforman el </br>
cuerpo humano:</br></br>

 

La columna vertebral es el soporte del cuerpo, y la que permite la movilidad del torso. Su forma erguida diferencia</br>
 al humano de otras especies, pero no se trata de una línea recta, sino curvilínea, su disposición le da a la pelvis</br>
  y a la caja torácica una ligera inclinación. Para visualizarla mejor, se divide en tres partes:</br></br>

1.Cervical – que sostiene y provee movilidad a la cabeza.</br>
2.Dorsal o torácica – que sostiene las costillas.</br>
3.Lumbar – un poco antes de llegar a la pelvis, conectada al sacro.


</br> </br>
