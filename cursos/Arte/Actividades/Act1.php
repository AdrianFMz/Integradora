El tercer ejercicio te ayudará a desarrollar tus habilidades para replicar colores. Usando un círculo cromático complejo </br> 
como referencia, intenta recrear los colores terciarios que veas mezclando los colores primarios (es decir, rojo, </br>
amarillo y azul). Tómate todo el tiempo que necesites para mezclar las pinturas, y no te preocupes si te toma algunos  </br>
intentos hacerlo bien, ya que encontrar la combinación correcta puede ser difícil. </br> </br>

Sin embargo, una vez que lo hayas practicado un poco, puedes usar este ejercicio para replicar otras muestras que  </br>
encuentres, como ropa, pintura de pared y embalajes. Esto también mejorará enormemente tu habilidad para capturar los  </br>
colores de la vida real. </br> </br>

