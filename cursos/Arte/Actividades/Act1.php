<h1>Teoría del Color:</h1> </br> </br>




    
<p>
<h5>PRACTICA LA MEZCLA DE COLORES.</h5></br> </br>

Una vez que hayas hecho tu círculo cromático básico, puedes practicar y expandirlo. Empieza de nuevo creando un círculo  </br>
básico con los colores primarios. Después, mézclalos para crear los colores secundarios: verde, naranja y púrpura.  </br>
En este punto, cuando mezcles cualquier color primario con uno secundario (o dos colores secundarios) crearás los que se  </br>
conoce como un color terciario. Esta es una gran manera de descubrir cuántos colores puedes crear con tan solo los tubos  </br>
de pintura esenciales. </br> </br>

A medida que crees estos colores terciarios, acomódalos en el círculo. Dependiendo de los colores que hayas creado, tu  </br>
nuevo círculo puede ser muy diferente al de tu foto de referencia, así que no te preocupes por hacerlo perfecto. Todo  </br>
esto es para practicar. </br> </br>

 


