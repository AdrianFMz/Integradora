<h5>Ejercicio 3</h5></br> 
1. Dibuja vehículos geométricos (coches, automóviles...) atendiendo a una perspectiva libre sin puntos de fuga, </br> 
sin dejar de trabajar el volumen.</br> </br>

2. Comienza dibujando la parte frontal del auto, donde va el radiador con los faros.</br> </br>

3. Desarrolla el perfil usando la perspectiva diagonal.</br> </br>

4. Siempre que sigas las reglas, el volumen funcionará.</br> </br>

5. Sigue con la composición hasta llenar la página

