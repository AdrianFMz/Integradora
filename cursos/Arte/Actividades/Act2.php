<h1>Perspectiva</h1>




<p>

<h5>Ejercicio 1</h5></br> 
1. Dibuja un punto en el centro de una hoja de papel (ese será tu punto de fuga).</br> </br>

2. Dibuja algunos cuadrados alrededor del punto, todos de diferentes tamaños y en distintas posiciones</br>
 (se recomienda hacerlos sin regla, para entrenar la capacidad de hacer paralelogramos con lados iguales).</br> </br>

3. Una vez terminados, toma una regla y traza líneas de fuga desde cada una de las aristas de los cuadrados </br> 
hacia el punto de fuga. Empieza con los cuadros que estén más cercanos al punto.</br> </br>

4. No pises las líneas si una se cruza con otra. Esto permite lograr una tridimensionalidad más limpia.

</br> </br>
