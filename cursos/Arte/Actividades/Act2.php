<h5>Ejercicio 2</h5></br> 
1. Dibuja un punto en el centro de una hoja de papel (tu punto de fuga).</br> </br>

2. Dibuja formas geométricas más complejas que un cuadrado, por ejemplo: una estrella, un triángulo, un rombo... </br>
incluso pueden ser formas abstractas, siempre que tengan aristas y lados rectos.</br> </br>

3. Traza líneas que vayan desde las artistas de cada figura hasta el punto de fuga. Empieza por las líneas pertenecientes </br>
a las figuras más cercanas al punto para que no se solapen.</br> </br>

4. Para saber de qué arista se puede generar una línea, basta con medir con la regla y darse cuenta si sale una línea que </br> 
no pise la figura.


</br> </br>
