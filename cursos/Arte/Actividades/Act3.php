<h1>Luz y Sombra:</h1>

<p>

La luz nos permite ver los colores y todo lo que nos rodea. Gracias a ella logramos diferenciar las formas y </br>
el volumen de los objetos. En esta oportunidad, quiero compartir algunos puntos que nos ayudarán a mejorar la </br>
interpretación de luces y sombras en nuestros dibujos.</br></br>

<h5>El clásico ejemplo de la esfera sobre una mesa nos muestra la interacción básica entre claros y oscuros:</h5></br></br>

<img src="http://localhost/integradora/imagenes/luz1.png" width="30%" height="30%" class="d-inline-block align-top" alt="">
</br></br>

<h5>Del lado de la luz tenemos:</h5></br></br>
• Luz directa: Es la zona del objeto que está de cara a la luz y que se encuentra más iluminada.</br></br>


<img src="http://localhost/integradora/imagenes/luz2.png" width="30%" height="30%" class="d-inline-block align-top" alt="">
</br></br>

<h5>Tono medio:</h5></br></br> Aquí, la escala de valores empieza a disminuir a un punto neutro entre la claridad y la oscuridad.</br>
 También se conoce como el color/tono “base” del objeto.</br></br>

 <img src="http://localhost/integradora/imagenes/luz3.png" width="30%" height="30%" class="d-inline-block align-top" alt="">
</br></br>

<h5>En el lado oscuro:</h5></br>
• Sombra cimera: Es el contorno de la sombra (también se le conoce como Core Shadow o Edge Shadow). En el caso de la</br>
 esfera, podemos observar que tiene una forma anillada que va acorde a su superficie. Esta zona suele ser más oscura</br>
  porque ni la luz directa ni la luz que rebota de la mesa alcanzan a tocar esta “cara” del objeto.</br></br>

• Tono medio oscuro (en inglés, Middle Dark Tone): es un tono neutro entre los más claros y los más oscuros de la sombra.</br></br>

• Sombra proyectada: es la silueta que dibuja nuestro objeto sobre la superficie en la que se encuentra. Su forma puede </br>
extenderse o contraerse acorde al ángulo de los rayos de luz.</br></br>

<img src="http://localhost/integradora/imagenes/luz4.png" width="30%" height="30%" class="d-inline-block align-top" alt="">
</br></br>

<h5>¡No nos olvidemos de la reflexión!</h5></br>
Es cuando la luz, al chocar contra una superficie, cambia de dirección. Cualquier superficie puede reflejar la luz</br>
 (algunas más que otras), por eso es muy difícil que las sombras sean 100% oscuras en el mundo real (¡a menos que no</br>
  haya luz en absoluto!).</br></br>

  <h5>Brillo:</h5></br>Es el reflejo de la fuente de luz sobre el objeto, su posición se determina desde nuestro punto de vista – </br>
si nos movemos, el brillo también cambiará de lugar. En la escala de valores, posee un tono más claro que el de la luz</br>
 directa, pero recuerda que la intensidad de este depende del material del objeto – el metal puede tener un brillo más</br>
  evidente que la madera, por ejemplo.</br></br>

  <img src="http://localhost/integradora/imagenes/luz5.png" width="30%" height="30%" class="d-inline-block align-top" alt="">
</br></br>

  <h5>Luz reflejada:</h5></br> Es la luz que, al rebotar de la mesa, cambia de dirección proyectándose en la esfera. Podemos apreciarlo mejor con </br>
  el ejemplo a color:</br></br>

  <img src="http://localhost/integradora/imagenes/luz6.png" width="30%" height="30%" class="d-inline-block align-top" alt="">
</br></br>

En el dibujo se definen tanto luces como sombras cuando se otorgan valores a las figuras. Si representamos estos valores</br>
 en una escala, podemos identificar el nivel de claridad u oscuridad que posee cada tono de gris.</br></br>

El truco está en mantener un buen balance entre claros y oscuros para definir los detalles de la imagen con éxito,</br>
 pues ambos trabajan en equipo – la luz define la sombra y la sombra define la luz.</br></br>
 <img src="http://localhost/integradora/imagenes/luz7.png" width="30%" height="30%" class="d-inline-block align-top" alt="">
</br></br>

A la izquierda, la imagen está subexpuesta, los tonos son muy oscuros y similares entre sí, se pierden detalles </br>
y se aplana la imagen.</br></br>

En la derecha, es el caso contrario, la imagen está sobreexpuesta, la cantidad de luz es cegadora y casi no hay </br>
distinción entre la figura y el fondo. La transición de tonos es muy tímida, los “más oscuros” siguen siendo muy claros.</br></br>

<img src="http://localhost/integradora/imagenes/luz8.png" width="30%" height="30%" class="d-inline-block align-top" alt="">
</br></br>


<h5>¡Identifica los planos!</h5></br></br>
Muchos jóvenes artistas tienden a crear “efectos de sombreado” que no representan correctamente el volumen de </br>
los objetos.</br></br>

Si queremos sombrear el rostro humano, por ejemplo, sería útil conocer algo de su anatomía, observar su estructura </br>
y construirlo a base de formas geométricas simples (cubos, cilindros, esferas…) que nos permitan identificar, sin</br>
 mucha dificultad, los planos que están de cara a la luz, las áreas oscuras y las zonas que se ven afectadas por</br>
  la luz que rebota desde otra superficie.</br></br>
  
