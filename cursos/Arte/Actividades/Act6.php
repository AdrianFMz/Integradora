<h5>4.      Practica con figuras geométricas</h5></br></br>
Una de las bases del dibujo es usando figuras geométricas básicas, eso te ayudara a dibujar formas o modelos que</br>
 se te dificultan, y nada mejor que usar estas figuras para mejorar el trazo.</br></br>

Primero empieza dibujando las figuras geométricas básicas, busca solo dibujar los contornos, puedes jugar con el </br>
tamaño, pero ya que buscamos mejorar nuestras habilidades te propongo dibujar las figuras geométricas en 3D.</br></br>

Por ejemplo, en vez de dibujar un cuadrado dibuja un cubo, o un cilindro en vez de un rectángulo, un cono en </br>
representación de un triángulo y una esfera en vez de un circulo, con estas formas podrás dibujar jarras, balones, </br>
velas, cajas y simplificar otros objetos.

