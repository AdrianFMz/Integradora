<h5>1.      La línea</h5>
Muchas personas subestiman el poder de las líneas en un dibujo, pero el uso de esta adecuadamente puede darles un</br>
 sello especial a tus dibujos, teniendo una buena línea automáticamente tendrás buen pulso, y seguro has pensado </br>
 que tu pulso es pésimo o que podría ser mejor.</br></br>


 Por eso empezaremos con algo tan sencillo como la línea, tu trazo debe ser firme, dinámico y sin sobresaltos,</br>
  si en cambio tu mano se pone rígida o tienes miedo a la hoja en blanco, no te preocupes, eso lo podemos superar </br>
  practicando cada día, veamos algunos consejos para hacerlo posible.</br></br>

1.No apoyes tu muñeca sobre el papel, esto se llama dibujar a mano alzada, al principio será difícil, pero con el tiempo será más fácil, ten paciencia</br>
2.Antes de dibujar relaja tu mano, puedes hacer ejercicios circulares en tu muñeca, así dibujaras con soltura</br>
3.Evita hacer pequeños trazos o entre cortados, muchos dibujantes usan esta línea que en vez de ayudar puede hacer todo mucho más difícil</br>
4.No remarques los trazos, procura hacer solo una línea, la primera línea tiene mucho dinamismo y claridad.</br></br>



