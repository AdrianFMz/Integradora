 <h5>6.      Identifica formas de un objeto</h5></br></br>
Vamos a empezar a entrenar tu ojo de artista, para ello este ejercicio te ayudara en el proceso, elije un objeto y </br>
tomate un minuto para observarlo bien ¿Cuántos cilindros tiene, esferas, rectángulos o cuadrados tiene el objeto?</br></br>

Ahora vamos a reducir los objetos a figuras simples, haremos este ejercicio con un libro, una naranja y una jarra, veamos el paso a paso:</br></br>

1.Primero dibuja n rectángulo, un cilindro y un circulo, define los contornos y agrega un par de líneas para darle relieve, un aspecto en 3D</br></br>
2.Empieza a agregar detalles, como el brazo de la jarra, la hendidura de la naranja o a ondular los bordes del libro</br></br>
3.Ahora solo borra las líneas guías del inicio, y listo

