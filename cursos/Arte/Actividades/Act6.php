<h5>3.      La repetición</h5>
Ahora veamos la precisión y la mejor forma de mejorarlo es usando la repetición, veamos cuales son los pasos</br>
 para mejorar la precisión en el dibujo.</br></br>

1.En una hoja dibujar una línea recta o circular con poca presión</br>
2.Ahora intentar dibujar lo mismo sobre ese mismo trazado</br>
3.Realiza otro trazado igual que los primeros pero esta vez aumenta la presión
</br></br>
Seguro notaras que es difícil dibujar el mismo trazo encima de otro, procura concentrarte y no salirte de la línea,</br>
también puedes jugar con el tipo de trazos para aumentar la dificultad. Con este simple ejercicios mejoraras tu</br>
 precisión a la hora de dibujar.

 </br> </br>
