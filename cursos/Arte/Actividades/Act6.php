<h5>2.      Mejorar el control del lápiz</h5></br>
En este ejercicio la intención es controlar la dirección del lápiz, es un ejercicio muy sencillo que se usa en</br>
 niños en sus primeros años, la intención es mejorar la motricidad fina, tan solo debes de tomar una hoja y dibujar</br>
  puntos sobre toda la hoja en diferentes lugares, y con tu lápiz unir esos puntos con una línea.</br></br>

Al inicio puedes usar pequeños puntos y para aumentar la dificultad aumentas la cantidad de puntos o tratar de unir </br>
todos los puntos sin levantar el lápiz, es decir, haciendo una sola línea rápida por toda la hoja.

</br> </br>
