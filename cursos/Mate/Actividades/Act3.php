<h1>Manipulación de expresiones algebraicas:</h1>




    <p>
    El aspecto inicial de algunas ecuaciones podría hacer pensar que son difíciles de resolver.</br> 
    En cambio, en muchos casos, una vez simplificada, la ecuación resultante puede resolverse fácilmente. </br>
    Por ello siempre es recomendable simplificarlas al máximo, expresándolas, si es posible, en forma normal. </br>
    Además, si aparecen fracciones o raíces, es conveniente intentar eliminarlas en el proceso </br>
    de simplificación siguiendo estas recomendaciones:</br>
   
      
    </br></br>
    Si aparecen denominadores, deben multiplicarse ambos miembros de la ecuación por dichos denominadores.
    </br>
    </br>
    Por ejemplo, la ecuación:
    </br>
    </br>
    2x−1x−2=5x−2
    </br>
    puede simplificarse multiplicando ambos lados por x−2:
    </br>
    (x−2)2x−1x−2=(x−2)(5x−2)
    </br>
    </br>
    con lo que queda una ecuación de segundo grado:
    </br>
    2x−1=(x−2)(5x−2)
    </br>
    </br>
    Si aparecen raíces, deben eliminarse. Es recomendable aislarlas, sucesivamente, en uno de los miembros de la ecuación y, </br>
    después, elevar al cuadrado los dos miembros de la igualdad. Por ejemplo, para resolver:
    </br>
    </br>
    √3x^2−3x+1+4 = 2x+2
    </br>
    </br>
    debe aislarse la raíz en el miembro de la izquierda:
    </br>
    </br>
    √3x^2−3x+1 = 2x−2
    </br>
    </br>
    y elevar al cuadrado:
    </br>
    </br>
    (√3x^2−3x+1)2 = (2x−2)^2
    </br>
    </br>
    con lo que queda una ecuación de segundo grado:
    </br>
    </br>

    3x^2−3x+1=(2x−2)^2
    </br>
    </br>
    Al resolver una ecuación modificada es imprescindible comprobar si las soluciones encontradas son también </br>
    soluciones de la ecuación original, puesto que la ecuación original y la modificada no son siempre equivalentes.</br></br>
     Por ejemplo, al multiplicar por x−2 la primera ecuación, si la x=2, entonces se multiplicaría por 0.</br></br>

