<h1>Resolución de sistemas de ecuaciones:</h1>




    <p>
    Un sistema de ecuaciones es un conjunto de ecuaciones (en nuestro caso serán dos ecuaciones) y varias incógnitas </br>
    (en nuestro caso dos) que aparecen en una o varias de las ecuaciones.</br></br>

    Una ecuación que tiene más de una incógnita nos informa de la relación que existe entre éstas. Por ejemplo, </br>
    la ecuación x - y = 0 nos dice que x e y son el mismo número.</br></br>

    No podemos resolver una ecuación con dos incógnitas ya que una de ellas queda en función de la otra. Por ejemplo, si tenemos la ecuación</br>
     x - 2y = 0 y aislamos x obtenemos que x = 2y. Es decir, que el valor de x es el doble que el de y. Pero continuamos sin saber los valores de x e y.</br></br>

    Para poder resolver un sistema de N incógnitas necesitamos tener N ecuaciones. En realidad, también necesitamos </br>
    que las ecuaciones sean linealmente independientes, pero no tendremos en cuenta esta necesidad en este nivel.</br></br>

    En esta sección tenemos problemas cuya resolución requieren el planteamiento de sistemas de ecuaciones de dimensión 2 (dos ecuaciones y dos incógnitas). Si no recordamos cómo resolver los sistemas (igualación, reducción y sustitución), podemos visitar la página resolución de sistemas (métodos básicos).
    </p>
   

    <p>
    Problema 1 dificultad</br></br>
Dos números suman 25 y el doble de uno de ellos es 14. ¿Qué números son?</br></br>

SOLUCIÓN:</br>
En primer lugar, definimos las incógnitas:</br></br>

x es uno de los números,</br></br>
y es el otro número.</br></br>

Por un lado, sabemos que los dos números suman 25, lo cual nos proporciona la primera ecuación:</br></br>
x + y = 25</br></br>

Por otro lado, también sabemos que el doble de uno de los números es 14, lo cual proporciona la segunda ecuación:</br></br>
2x = 14</br></br>

Ya tenemos 2 incógnitas y 2 ecuaciones, suficiente para plantear y resolver el sistema de ecuaciones:</br></br>
 {  x + y = 25</br></br>
    | 2x = 14</br></br>

    Aplicamos el método de sustitución, despejando la x en la segunda ecuación y sustituyendo en la primera.</br></br>

Despejamos la x en la segunda ecuación:</br></br>
2x = 14</br></br>
x = 14/2</br></br> 
x = 7</br></br>



Observad que ya tenemos el valor de una incógnita: </br></br>
x
=
7

</br></br>
Sustituimos en la primera ecuación (escribimos 7 en lugar de 
x
) y resolvemos la ecuación:</br></br>


 x + y = 25</br></br>
 7 + y = 25</br></br>
 y = 25 - 7</br></br>
 y = 18</br></br>
 </p>
 La solución del sistema es 
x
