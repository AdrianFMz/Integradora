<h1>Introducción a las funciones cuadráticas: </h1>




    <p>
    Una función cuadrática es aquella en la que la variable independiente (x) aparece elevada al cuadrado. Estas funciones </br>
    tienen la siguiente forma:</br></br>

Función cuadrática</br></br>

Las letras “a”, “b” y “c” son constantes reales. Tanto la constante “b” como la constante “c” pueden ser cero. La gráfica de una</br>
función cuadrática se llama parábola. Una característica de las funciones cuadráticas es que a igual variación de la variable “x” </br>
corresponden diferentes variaciones de la variable “y”.</br></br>
   
    </br>
    <img src="http://localhost/integradora/imagenes/grafica4.png" width="30%" height="30%" class="d-inline-block align-top" alt="">
    </br>
      

Parámetros de una función cuadrática</br></br>
El valor del coeficiente “a” determina la apertura de la parábola. Cuanto mayor sea su valor será más cerrada, ya que aumenta o</br>
disminuye su valor más rápidamente.</br></br>

El signo del coeficiente “a” determina la orientación de la parábola. Está orientada hacia arriba si el signo es positivo mientras </br>
que está orientada hacia abajo si el signo es negativo.</br></br>

Los coeficientes “b” y “c” determinan juntos el eje de simetría.</br></br>

El coeficiente “c” determina el punto en el que la parábola corta al eje “y”. Esto es similar a la ordenada al origen en una recta. </br></br>
Debemos tener en cuenta que no necesariamente en el punto “c” está vértice (mínimo o máximo).</br></br>


