<h1>Graficación de funciones lineales:</h1>




    <p>
        </br>
        </br>
        Las funciones lineales son aquellas funciones que tienen la forma  y = mx + b ; que también se pueden escribirde la forma f(x) = mx + b.</br> 
        Veamos algunas características importantes de la función lineal, junto a los ejercicios y aplicaciones que hemos preparado.</br>
        </br>
        Elementos de la función lineal
        En la función lineal, que siempre tiene la forma y = mx + b ; tenemos los siguientes elementos:
        </br>
        </br> 
        x: variable independiente.</br> 
            y: variable dependiente (su valor depende del valor de x).</br> 
            m: pendiente.</br> 
            b: corte con el eje y, u ordenada de origen.</br> 
        </br>
        Veamos algunos ejemplos de funciones lineales y no lineales:

