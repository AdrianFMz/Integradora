<h1>Guía para iniciar</h1>

<p>
</br>
<h3>Parte 1: Conceptos Básicos</h3></br></br>

<h5>Aritmética:</h5></br>

Números naturales, enteros, racionales y reales.</br>
Operaciones básicas: suma, resta, multiplicación y división.</br>
Propiedades de las operaciones: conmutativa, asociativa y distributiva.</br>
Fracciones, decimales y porcentajes.</br>
Potenciación y radicación.</br></br>

<h5>Álgebra:</h5></br>

Expresiones algebraicas.</br>
Ecuaciones e inecuaciones de primer y segundo grado.</br>
Sistemas de ecuaciones.</br>
Funciones lineales y cuadráticas.</br></br>

<h5>Geometría:</h5></br>

Puntos, rectas, planos y ángulos.</br>
Triángulos, cuadriláteros y otros polígonos.</br>
Círculos y esferas.</br>
Teorema de Pitágoras.</br>
Áreas y volúmenes.</br></br>

<h5>Trigonometría:</h5></br>

Seno, coseno y tangente.</br>
Leyes de los senos y cosenos.</br>
Triángulos rectángulos y oblicuángulos.</br></br>

<h3>Parte 2: Ejercicios y Prácticas</h3></br>

Problemas de aritmética:</br></br>
Calcular el resultado de operaciones básicas.</br>
Resolver problemas con fracciones, decimales y porcentajes.</br>
Aplicar las propiedades de las operaciones.</br></br>

<h5>Problemas de álgebra:</h5></br>
Simplificar expresiones algebraicas.</br>
Resolver ecuaciones e inecuaciones.</br>
Resolver sistemas de ecuaciones.</br>
Graficar funciones lineales y cuadráticas.</br></br>

<h5>Problemas de geometría:</h5></br>
Calcular perímetros y áreas de figuras geométricas.</br>
Aplicar el teorema de Pitágoras.</br>
Calcular volúmenes de sólidos geométricos.</br></br>

<h5>Problemas de trigonometría:</h5></br>
Calcular razones trigonométricas.</br>
Resolver problemas con triángulos rectángulos y oblicuángulos.</br></br>

