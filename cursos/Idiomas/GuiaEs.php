<h1>Guia para iniciar</h1></br>

Resumen de 6 temas de inglés:</br>
Aprenderás sobre 6 temas diferentes de inglés: verbos modales, futuro, </br>
pasado continuo, conectores "while" y "when", y más.</br>
Obtendrás una introducción a cada tema, incluyendo ejemplos y explicaciones.</br>
Podrás utilizar esta información como base para aprender más sobre cada tema.</br></br>

<h4>1.Pasado continuo:</h4>
-Aprenderás qué es el pasado continuo y cómo se utiliza para describir acciones </br>
que estaban ocurriendo en un momento específico en el pasado.</br>
-Verás ejemplos de cómo se forma el pasado continuo y cómo se usa en oraciones.</br>
-Podrás utilizar esta información para hablar sobre eventos que sucedieron en </br>
el pasado y que duraron un tiempo.</br></br>

<h4>2. Past Continuous Connectors While and When:</h4>
-Aprenderás la diferencia entre "while" y "when" en pasado continuo.</br>
-Practicarás el uso de estos conectores en oraciones y ejercicios.</br>
-Podrás utilizar esta información para mejorar tu fluidez y precisión al hablar inglés.</br></br>

<h4>3. Modal verbs Ability Challanges:</h4>
-Aprenderás cómo usar los verbos modales para expresar habilidad y desafío.</br>
-Practicarás el uso de estos verbos en oraciones y ejercicios.</br>
-Podrás utilizar esta información para comunicarte de manera más efectiva en inglés.</br></br>

<h4>4. Modal verbs Posibility, May or Might:</h4>
-Aprenderás la diferencia entre "may" y "might" para expresar posibilidad.</br>
-Practicarás el uso de estos verbos modales en oraciones y ejercicios.</br>
-Podrás utilizar esta información para hablar con mayor precisión </br>
sobre el futuro en inglés.</br></br>

<h4>5. The future "be going to":</h4>
-Aprenderás qué es "be going to" y cómo se utiliza para hablar del futuro.</br>
-Practicarás el uso de "be going to" en oraciones y ejercicios.</br>
-Podrás utilizar esta información para hablar sobre tus planes e intenciones en inglés.</br></br>

<h4>6. Present continuous for future:</h4>
-Aprenderás cómo usar el presente continuo para hablar del futuro.</br>
-Practicarás el uso del presente continuo en oraciones y ejercicios.</br>
-Podrás utilizar esta información para hablar sobre eventos planificados </br>
o programados en inglés.</br></br>

En general, estos 6 temas te proporcionarán una base sólida para comprender </br>
y usar el inglés en una variedad de contextos.</br></br>

Recuerda que la mejor manera de aprender es practicar. </br>
Intenta usar estos temas en tu propio idioma y busca oportunidades para hablar inglés con otras personas.</br></br></br>


