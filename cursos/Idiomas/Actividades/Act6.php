<h1>¿Qué es el "Present Continuous" para el futuro?</h1></br>


El presente continuo, también conocido como "Present Progressive", </br>
se puede usar para hablar de acciones que están planeadas o programadas para el futuro. </br>
Es una forma informal de expresar el futuro en inglés.</br></br>

<h3>Forma:</h3>

Sujeto + am / is / are + verbo en -ing</br></br>

<h3>Ejemplos:</h3>

I am leaving tomorrow. (Me voy mañana.)</br>
She is starting her new job next week. (Ella comienza su nuevo trabajo la semana que viene.)</br>
They are getting married next year. (Se casan el año que viene.)</br></br>

<h3>Diferencias entre "Present Continuous" para el futuro y "Will":</h3>

"Present Continuous" se usa para hablar de acciones planeadas o programadas.</br>
"Will" se usa para hablar de decisiones espontáneas, predicciones y promesas.</br></br>

