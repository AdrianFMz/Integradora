<h1>¿Qué es el pasado continuo?</h1></br>


El pasado continuo se utiliza para hablar de acciones que estaban</br>
en curso en un momento específico en el pasado. Se forma utilizando </br>
el verbo auxiliar "to be" en pasado simple ("was" o "were") y el</br>
verbo en gerundio (-ing).</br></br>

<h3>Ejemplos:</h3></br>

I was studying when you called. (Estaba estudiando cuando llamaste).</br>
They were playing football when it started to rain. </br>
(Estaban jugando al fútbol cuando empezó a llover).</br></br>

