<h3>Diferencias entre el pasado simple y el pasado continuo:</h3></br>

El pasado simple se utiliza para hablar de acciones que ocurrieron en un </br>
momento específico en el pasado. El pasado continuo se utiliza para hablar de </br>
acciones que estaban en curso en un momento específico en el pasado.</br></br>

<h3>Ejemplo:</h3></br>

I went to the store yesterday. (Fui a la tienda ayer). (Acción puntual)</br>
I was walking to the store when I saw you. (Iba a la tienda cuando te vi). (Acción en curso)</br></br>

