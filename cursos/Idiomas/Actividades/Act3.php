<h1>¿Qué son los verbos modales de habilidad y desafío?</h1></br>

Los verbos modales de habilidad y desafío se utilizan para expresar</br>
la capacidad o dificultad de realizar una acción. </br></br>
 
<h3>Los más comunes son:</h3></br>

Can: Se utiliza para expresar habilidad o permiso en el presente.</br>
Could: Se utiliza para expresar habilidad o permiso en el pasado o para hacer una sugerencia.</br>
May: Se utiliza para expresar permiso o posibilidad.</br>
Might: Se utiliza para expresar posibilidad o incertidumbre.</br>
Must: Se utiliza para expresar necesidad u obligación.</br>
Should: Se utiliza para expresar consejo o deber.</br></br>

<h3>Ejemplos:</h3></br>

Habilidad:</br>
I can speak English. (Puedo hablar inglés.)</br>
She could swim when she was a child. (Ella podía nadar cuando era niña.)</br>
You may use my computer. (Puedes usar mi computadora.)</br></br>

Desafío:</br>
This math problem is difficult. I might not be able to solve it.(Este problema de matemáticas es difícil. Es posible que no pueda resolverlo.)</br>
You must finish your homework before you go out.(Debes terminar tu tarea antes de salir.)</br>
We should help those in need.(Deberíamos ayudar a los necesitados.)</br></br>

