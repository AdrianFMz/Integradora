<h1>¿Qué es "be going to"?</h1> </br>

<h3>"Be going to" es una forma de expresar el futuro en inglés. </br>
    Se utiliza para hablar de:</h3></br>

Planes e intenciones: acciones que has decidido hacer en el futuro.</br>
Predicciones: acciones que crees que van a ocurrir en el futuro.</br></br>

<h3>Forma:</h3>

Sujeto + be going to (am, is, are) + verbo en infinitivo</br></br>

<h3>Ejemplos:</h3>

I am going to study English tomorrow. (Voy a estudiar inglés mañana.)</br>
She is going to be a doctor. (Ella va a ser doctora.)</br>
They are going to the beach this weekend. (Ellos van a la playa este fin de semana.)</br></br>

<h3>Diferencias entre "be going to" y "will":</h3></br>

"Be going to" se usa para hablar de planes e intenciones.</br>
"Will" se usa para hablar de decisiones espontáneas, predicciones y promesas.</br></br>

