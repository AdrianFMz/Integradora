<h1> Conectores "while" y "when" en pasado continuo:</h1></br>

<h5>¿Qué diferencia hay entre "while" y "when" en pasado continuo?</h5></br>

Los conectores "while" y "when" se utilizan para unir dos oraciones en </br>
pasado continuo, pero tienen diferentes significados:</br></br>

While: Se utiliza para indicar que dos acciones ocurrían al mismo tiempo en el pasado.</br></br>

When: Se utiliza para indicar que una acción interrumpió a otra que estaba en curso en el pasado.</br></br>

<h3>Ejemplos:</h3></br>

While:</br>
I was studying while my brother was playing video games. (Estaba estudiando mientras mi hermano jugaba videojuegos.)</br>
The rain was falling while we were walking to the park. (Estaba lloviendo mientras caminábamos al parque.)</br></br>

When:</br>
I was cooking dinner when the phone rang. (Estaba cocinando la cena cuando sonó el teléfono.)</br>
I was watching a movie when I fell asleep. (Estaba viendo una película cuando me dormí.)</br></br>

