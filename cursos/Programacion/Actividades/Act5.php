<h1>¿Qué es C++?</h1> </br>

C++ es un lenguaje de programación de alto rendimiento, versátil </br>
y ampliamente utilizado para desarrollar aplicaciones de alto </br>
rendimiento, software de sistema y desarrollo de juegos.</br></br>

<h3> Características clave de C++: </h3></br>

<h5> Programación orientada a objetos (POO): </h5>
C++ te permite crear objetos</br>
(estructuras de datos) con atributos (datos) y métodos (funciones) </br>
que operan sobre esos datos. Esto promueve la reutilización del código y la modularidad.</br></br>

<h5>Programación procedural:</h5> 
Puedes dividir problemas complejos en procedimientos </br>
más pequeños y paso a paso, con un enfoque en las funciones y el flujo de control.</br></br>

<h5>Gestión de memoria: </h5>
C++ proporciona una gestión manual de la memoria mediante </br>
punteros, lo que te da un control preciso sobre la asignación y desasignación </br>
de memoria. Esto puede ser eficiente, pero requiere un manejo cuidadoso para </br>
evitar fugas de memoria.</br></br>

<h5>Plantillas:</h5> 
Las plantillas te permiten escribir código genérico que </br>
puede funcionar con diferentes tipos de datos sin duplicar código.</br></br>

