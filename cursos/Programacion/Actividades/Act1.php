<h1>¿Qué es HTML?</h1></br>

HTML significa HyperText Markup Language (Lenguaje de Marcado de Hipertexto). </br>
Es un lenguaje de marcado que se utiliza para crear páginas web. El lenguaje </br>
HTML define la estructura de una página web y su contenido, como texto, </br>
imágenes, vídeos y formularios.</br></br>

<h3>¿Cómo funciona HTML?</h3></br>

HTML se compone de etiquetas. Las etiquetas son palabras clave que se colocan </br>
entre corchetes angulares (< >). Las etiquetas se utilizan para definir </br>
diferentes elementos de una página web, como el título, la cabecera, </br>
los párrafos, las imágenes, etc.</br></br>

