<h1> ¿Qué es CSS?</h1></br>

CSS significa Cascading Style Sheets (Hojas de Estilo en Cascada). </br>
Es un lenguaje de diseño web que se utiliza para dar estilo a las páginas web. </br>
CSS define cómo se muestran los elementos de una página web, como el color, </br>
la fuente, el tamaño, el diseño y la posición.</br></br>

<h3> ¿Cómo funciona CSS?</h3></br>

CSS se compone de reglas. Las reglas CSS se utilizan para definir </br>
cómo se debe mostrar un elemento HTML. Una regla CSS se compone </br>
de un selector y una declaración de estilo.</br></br>

