<h1> Introducción a JavaScript</h1></br>

<h3>¿Qué es JavaScript?</h3></br>

JavaScript es un lenguaje de programación de alto nivel, dinámico e </br>
interpretado. Se utiliza principalmente para crear páginas web </br>
interactivas, aunque también se puede usar para desarrollar aplicaciones </br>
del lado del servidor, aplicaciones móviles y juegos.</br></br>

<h3>¿Cómo funciona JavaScript?</h3></br>

JavaScript se ejecuta en el navegador web del cliente. Cuando un usuario </br>
solicita una página web que contiene código JavaScript, el navegador </br>
interpreta el código y ejecuta las instrucciones.</br></br>

