<h1>¿Qué es PHP?</h1></br>

PHP significa Hypertext Preprocessor (Preprocesador de Hipertexto). </br>
Es un lenguaje de scripting del lado del servidor que se utiliza </br>
para crear páginas web dinámicas. PHP se puede incrustar en código </br>
HTML y se ejecuta en el servidor antes de que la página web </br>
se envíe al navegador del cliente.</br></br>

<h3>¿Cómo funciona PHP?</h3></br>

PHP se ejecuta en un servidor web. Cuando un usuario solicita una </br>
página web que contiene código PHP, el servidor ejecuta el código </br>
PHP y genera una página web HTML que se envía al navegador del cliente.</br></br>

