<h1>¿Qué es Java?</h1></br>

Java es un lenguaje de programación de propósito general, orientado </br>
a objetos, robusto y seguro. Se utiliza para desarrollar una amplia </br>
gama de aplicaciones, desde aplicaciones web y móviles hasta </br>
aplicaciones empresariales y juegos.</br></br>

<h3>Características clave de Java:</h3></br>

<h5>Orientado a objetos: </h5>
Java te permite crear objetos (estructuras de datos) </br>
con atributos (datos) y métodos (funciones) que operan sobre esos datos.</br>
Esto promueve la reutilización del código y la modularidad.</br></br>

<h5>Robustez: </h5>
Java está diseñado para ser un lenguaje robusto con características </br>
como la comprobación de tipos en tiempo de compilación y la gestión automática </br>
de memoria para evitar errores y fallos.</br></br>

<h5>Seguridad: </h5>
Java se diseñó con la seguridad en mente, con características como </br>
el sandboxing que ayudan a proteger las aplicaciones contra malware y ataques.</br></br>

<h5>Portabilidad: </h5>
El código Java se puede ejecutar en cualquier plataforma que </br>
tenga una máquina virtual Java (JVM) instalada, lo que lo hace muy portable.</br></br>

